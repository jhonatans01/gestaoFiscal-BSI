    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        e.setTitulo(jTextField1.getText());
        e.setDataI(new java.sql.Date(jDateChooser1.getDate().getTime()));
        e.setDataF(new java.sql.Date(jDateChooser2.getDate().getTime()));
        e.setLocal(jTextField2.getText());

        if (e.getTitulo().equals("") || e.getDataI().equals("")
                || e.getDataF().equals("") || e.getLocal().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher todos os campos");
        } else if (Principal.crud.editar(Principal.e)) {
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!");
            this.dispose();
            Principal.preencherTab();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao salvar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        tc = new Tema_curso();
        tc.setTitulo(JOptionPane.showInputDialog("Inserir o tema"));
        tc.setEvento_id(Principal.e.getId());
        if (tc.getTitulo() != null) {
            if (tcCrud.inserir(tc)) {
                JOptionPane.showMessageDialog(null, "Inserido com sucesso!");
                Principal.preencherTabTC(tc.getEvento_id());
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao inserir");
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

