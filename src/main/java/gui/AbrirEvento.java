    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        abrirCurso = AbrirCurso.getInstance();
        String[] a = new String[Principal.listaTc.size()];
        if (a.length == 0) {
            JOptionPane.showMessageDialog(null, "O evento não possui aulas cadastradas");
        } else {
            for (int i = 0; i < Principal.listaTc.size(); i++) {
                a[i] = Principal.listaTc.get(i).getTitulo();
            }
            preencherTbCursos();
            abrirCurso.setVisible(true);
        }
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int rowIndex = jTable1.getSelectedRow();

        if (rowIndex != -1) {
            tc = Principal.mttc.get(rowIndex);
            int jop = JOptionPane.showConfirmDialog(null, "Confirma a exclusão?");
            if (jop == JOptionPane.YES_OPTION) {
                if (tcCrud.deletar(tc)) {
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir");
                }
                Principal.preencherTabTC(Principal.e.getId());
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    public void editarAula() {

        int rowIndex = jTable1.getSelectedRow();

        if (rowIndex != -1) {
            tc = Principal.mttc.get(rowIndex);
            tc.setTitulo(JOptionPane.showInputDialog("Insira o novo título"));
            tc.setEvento_id(Principal.e.getId());

            if (tc.getTitulo() != null) {
                if (tcCrud.editar(tc)) {
                    JOptionPane.showMessageDialog(null, "Alterado com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao salvar");
                }
            }

            Principal.preencherTabTC(tc.getEvento_id());
        }
    }

