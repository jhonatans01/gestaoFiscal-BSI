    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int rowIndex = jTable1.getSelectedRow();

        if (rowIndex != -1) {
            AbrirEvento.ac = AbrirEvento.mtac.get(rowIndex);
            int jop = JOptionPane.showConfirmDialog(null, "Confirma a exclusão?");
            if (jop == JOptionPane.YES_OPTION) {
                if (AbrirEvento.crud.deletar(AbrirEvento.ac)) {
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir");
                }
                AbrirEvento.preencherTbCursos();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    public void editar() {
        int rowIndex = jTable1.getSelectedRow();

        if (rowIndex != -1) {
            AbrirEvento.ac = AbrirEvento.mtac.get(rowIndex);
            ec = EditarCurso.getInstance();

            Tema_curso t = AbrirEvento.tcCrud.obter(AbrirEvento.ac.getTema_curso_id());
            for (int i = 0; i < Principal.listaTc.size(); i++) {
                if (Principal.listaTc.get(i).getTitulo().equals(t.getTitulo())) {
                    ec.jList1.setSelectedIndex(i);
                }
            }

            UtilOptions.converterSet(AbrirEvento.ac.getPontualidade(), ec.buttonGroup1);
            UtilOptions.converterSet(AbrirEvento.ac.getDominio_assunto(), ec.buttonGroup2);
            UtilOptions.converterSet(AbrirEvento.ac.getClareza(), ec.buttonGroup3);
            UtilOptions.converterSet(AbrirEvento.ac.getDistr_ch(), ec.buttonGroup4);
            UtilOptions.converterSet(AbrirEvento.ac.getMotiv_grupo(), ec.buttonGroup5);
            UtilOptions.converterSet(AbrirEvento.ac.getEscl_duvidas(), ec.buttonGroup6);
            ec.setVisible(true);
        }

    }

