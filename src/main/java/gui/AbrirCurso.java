    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jList1.getSelectedIndex() > -1 && buttonGroup1.getSelection() != null
                && buttonGroup2.getSelection() != null && buttonGroup3.getSelection() != null
                && buttonGroup3.getSelection() != null && buttonGroup4.getSelection() != null
                && buttonGroup5 != null && buttonGroup6 != null) {
            AbrirEvento.ac = new Aval_curso();
            AbrirEvento.ac.setEvento_id(Principal.e.getId());
            AbrirEvento.ac.setTema_curso_id(Principal.listaTc.get(jList1.getSelectedIndex()).getId());
            AbrirEvento.ac.setPontualidade("" + UtilOptions.converterGet(buttonGroup1));
            AbrirEvento.ac.setDominio_assunto("" + UtilOptions.converterGet(buttonGroup2));
            AbrirEvento.ac.setClareza("" + UtilOptions.converterGet(buttonGroup3));
            AbrirEvento.ac.setDistr_ch("" + UtilOptions.converterGet(buttonGroup4));
            AbrirEvento.ac.setMotiv_grupo("" + UtilOptions.converterGet(buttonGroup5));
            AbrirEvento.ac.setEscl_duvidas("" + UtilOptions.converterGet(buttonGroup6));

            if (AbrirEvento.crud.inserir(AbrirEvento.ac)) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao salvar");
            }
        }
        AbrirEvento.preencherTbCursos();
    }//GEN-LAST:event_jButton4ActionPerformed

