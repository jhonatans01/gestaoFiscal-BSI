    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int rowIndex = jTable1.getSelectedRow();

        if (rowIndex != -1) {
            e = mte.get(rowIndex);
            System.out.println(e.getId());
            int jop = JOptionPane.showConfirmDialog(null, "Confirma a exclusão?");
            if (jop == JOptionPane.YES_OPTION) {

                if (crud.deletar(e)) {
                    JOptionPane.showMessageDialog(null, "Excluido com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro ao excluir");
                }
                preencherTab();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        e.setTitulo(jTextField1.getText());
        e.setDataI(new java.sql.Date(jDateChooser1.getDate().getTime()));
        e.setDataF(new java.sql.Date(jDateChooser2.getDate().getTime()));
        e.setLocal(jTextField2.getText());

        if (e.getTitulo().equals("") || e.getDataI().equals("")
                || e.getDataF().equals("") || e.getLocal().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencher todos os campos");
        } else if (crud.inserir(e)) {
            JOptionPane.showMessageDialog(null, "Inserido com sucesso!");
            jTextField1.setText("");
            jTextField2.setText("");
            jDateChooser1.setDateFormatString("");
            jDateChooser2.setDateFormatString("");
            preencherTab();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao inserir");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

