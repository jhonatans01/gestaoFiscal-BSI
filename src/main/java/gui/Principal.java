    public static void preencherTab() {
        listaEventos = crud.obterTodos();
        mte = new ModelTEvento(listaEventos);
        jTable1.setModel(mte);
        jTable1.getTableHeader().setReorderingAllowed(false);
        jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_ALL_COLUMNS);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public static void preencherTabTC(Integer id) {
        listaTc = tcCrud.obterTodos(id);
        mttc = new ModelTTcurso(listaTc);
        ae.jTable1.setModel(mttc);
        ae.jTable1.getTableHeader().setReorderingAllowed(false);
        ae.jTable1.setAutoResizeMode(jTable1.AUTO_RESIZE_ALL_COLUMNS);
        ae.jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

    public void botaoAbrir() {
        int rowIndex = jTable1.getSelectedRow();

        if (rowIndex != -1) {
            e = mte.get(rowIndex);
            ae = AbrirEvento.getInstance();
            ae.jTextField3.setText("" + e.getId());
            ae.jTextField1.setText(e.getTitulo());
            ae.jTextField2.setText(e.getLocal());
            ae.jDateChooser1.setDate(e.getDataI());
            ae.jDateChooser2.setDate(e.getDataF());
            preencherTabTC(e.getId());
            ae.setVisible(true);
        }
    }
